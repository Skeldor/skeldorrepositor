# Импортируем модуль re для работы с регулярными выражениями
import re

# Запрашиваем пароль от пользователя
password = input("Введите пароль: ")

# Проверяем длину пароля
if len(password) < 8:
  print("Пароль слишком короткий. Он должен содержать не менее 8 символов.")
else:
  # Проверяем наличие заглавной буквы
  if not re.search("[A-Z]", password):
    print("Пароль не содержит заглавной буквы. Он должен содержать как минимум одну заглавную букву.")
  else:
    # Проверяем наличие строчной буквы
    if not re.search("[a-z]", password):
      print("Пароль не содержит строчной буквы. Он должен содержать как минимум одну строчную букву.")
    else:
      # Проверяем наличие цифры
      if not re.search("[0-9]", password):
        print("Пароль не содержит цифры. Он должен содержать как минимум одну цифру.")
      else:
        # Проверяем наличие специального символа
        if not re.search("[!@#$%^&*]", password):
          print("Пароль не содержит специального символа. Он должен содержать как минимум один специальный символ (например, !@#$%^&*).")
        else:
          # Если все условия выполнены, выводим сообщение о том, что пароль надежный
          print("Пароль надежный.")